# Download do SQLite:

# Acesse o site oficial do SQLite em sqlite.org.
# No menu, clique em "Download".
# Procure pela seção "Precompiled Binaries for Windows".
# Baixe o arquivo sqlite-tools-win32-x86-XXXXXXX.zip (onde XXXXXXX é a versão atual).
# Extrair os Arquivos:

# Uma vez baixado, extraia os arquivos do ZIP em uma pasta de sua escolha. Você poderia criar uma pasta chamada SQLite em C:\ ou em qualquer outro lugar que você prefira.
# Adicionar ao PATH:

# Para facilitar a execução do SQLite a partir de qualquer diretório no prompt de comando, adicione a pasta onde você extraiu os arquivos ao PATH do sistema:
# Clique com o botão direito do mouse no ícone do Meu Computador ou Este Computador e escolha Propriedades.
# Selecione o link Configurações avançadas do sistema.
# Clique no botão Variáveis de Ambiente.
# Na seção Variáveis de sistema, localize a variável Path e clique em Editar.
# Adicione o caminho da pasta onde você extraiu os arquivos do SQLite. Se certifique de separar cada entrada com um ;.
# Clique em OK para fechar cada uma das janelas.
# Verificar a Instalação:

# Abra o Prompt de Comando (cmd).
# Digite sqlite3 e pressione Enter.
# Se tudo estiver configurado corretamente, você deve ver o prompt do SQLite. Você pode digitar .exit para sair do SQLite.